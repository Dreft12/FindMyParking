package com.example.eapal.findmyparking;

public class MapMarkers {

    private double lat;
    private double lgn;

    public MapMarkers() {
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLgn() {
        return lgn;
    }

    public void setLgn(double lgn) {
        this.lgn = lgn;
    }
}
